def run_multi_part_writing(topic_list, output_file):
    combined_content = ""
    
    sys_prompt = """คุณคือผู้เชี่ยวชาญการเขียนวิทยานิพนธ์ระดับดุษฎีบัณฑิต (PhD) ที่เน้นความสละสลวยของภาษาไทย
กฎการทำงาน:
- ใช้ภาษาไทยระดับวิชาการสูงสุด (Academic Thai) เท่านั้น
- ห้ามมีภาษาอังกฤษปนในเนื้อหาเด็ดขาด (ห้ามมีภาษาอังกฤษในวงเล็บ)
- ใช้คำทับศัพท์ภาษาไทยที่ถูกต้อง (เช่น คาร์บอนฟุตพริ้นท์, ดิจิทัล, ฟล็กซ์โกราฟิก)
- อ้างอิงแหล่งที่มาเป็นภาษาไทยตามมาตรฐาน APA 7th
- ห้ามมีคำทักทาย หรือกระบวนการคิด เริ่มต้นที่เนื้อหาทันที
- หากมีการแทรกรูปภาพ ให้ใช้รูปแบบ ![ชื่อรูป](พาธรูปภาพ) ในตำแหน่งที่เหมาะสม"""

    for i, topic in enumerate(topic_list):
        print(f"[*] Writing Section {i+1}/10: {topic['title']}...")
        prompt = f"เขียนเนื้อหาวิทยานิพนธ์ หัวข้อ: {topic['title']}\nรายละเอียดที่ต้องครอบคลุม: {topic['desc']}\nความยาว: 500-600 คำ\nใช้ภาษาไทยล้วน"
        
        part_content = call_llm(prompt, sys_prompt, agent_key="writer")
        combined_content += f"\n\n{part_content}\n"
        
    with open(output_file, "w", encoding="utf-8") as f:
        f.write(combined_content)
    print(f"✅ บันทึกเนื้อหาทั้งหมดลงที่: {output_file}")

